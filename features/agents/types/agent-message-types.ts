/**
 * Agent definition and conversation message types — AI Matrx API
 *
 * This file answers: "what is the full structure of a message at every
 * point in an agent's lifecycle?"
 *
 * There are two distinct contexts where messages appear:
 *
 *   1. AGENT DEFINITION  — the messages[] array authored by the agent creator.
 *      These are the "priming" turns baked into the agent's stored definition.
 *      Only user and assistant roles are valid here. No tool calls, no thinking.
 *
 *   2. RUNTIME CONVERSATION — the live messages accumulated during an actual
 *      conversation. Includes all roles and all block types, including
 *      tool calls, tool results, and thinking (extended reasoning).
 *
 * For the user_input field on the request body (what the React app sends),
 * see message-types.ts which covers that surface area exclusively.
 *
 * ---
 * RELATIONSHIP TO message-types.ts
 * ─────────────────────────────────
 * ContentBlock (from message-types.ts) is the set of blocks a user can send.
 * ConversationBlock (this file) is the full superset that can appear in any
 * message in the database or in a streaming response. Every ContentBlock is
 * also a valid ConversationBlock, but not vice versa.
 *
 *   ContentBlock  ⊂  ConversationBlock
 */

