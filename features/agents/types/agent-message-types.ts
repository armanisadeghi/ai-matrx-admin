// SECTION 6 — Quick reference cheat-sheet
// =============================================================================
//
// ┌─────────────────────────────────────────────────────────────────────────┐
// │  CONTEXT                   │ TYPE TO USE                                │
// ├────────────────────────────┼───────────────────────────────────────────┤
// │ user_input in request body │ UserInput (message-types.ts)               │
// │ Agent definition messages  │ AgentDefinitionMessage[]                   │
// │ Stored conversation turn   │ ConversationMessage (or RoleMessage)       │
// │ Any block anywhere         │ ConversationBlock                          │
// │ User-sendable blocks only  │ ContentBlock (message-types.ts)            │
// │ Single text+image user msg │ UserMessage                                │
// │ Model response with tools  │ AssistantMessage                           │
// │ Tool execution results     │ ToolMessage                                │
// └────────────────────────────┴───────────────────────────────────────────┘
//
// ROLE RULES (enforced server-side)
// ──────────────────────────────────
//   system / developer  → NEVER in messages[]. Always in system_instruction.
//   user                → ContentBlock[] (all media + structured inputs)
//   assistant           → TextBlock | ThinkingBlock | ToolCallBlock |
//                         CodeExecutionBlock | CodeExecutionResultBlock |
//                         WebSearchCallBlock
//   tool                → ToolResultBlock[] (one per executed call)
//
// THINKING SIGNATURE RULE
// ────────────────────────
//   If a ThinkingBlock with a non-null signature is in the history,
//   it MUST be echoed back exactly as received when continuing the
//   conversation. Never strip or modify signatures. The server handles
//   this automatically — only relevant if you manually construct history.
//
// TOOL CALL / RESULT PAIRING
// ───────────────────────────
//   ToolCallBlock.id  ←→  ToolResultBlock.call_id
//   Every ToolCallBlock in an assistant message must be followed (eventually)
//   by a ToolResultBlock with a matching call_id in a "tool" role message.
