// features/notes/components/NoteTabBar.tsx
// Layer 4: Tab bar manager for an instance. Zero prop drilling — Redux selectors only.
"use client";

import React, { useRef, useEffect, useCallback } from "react";
import { Plus } from "lucide-react";
import { useAppDispatch, useAppSelector } from "@/lib/redux/hooks";
import {
  setInstanceActiveTab,
  addInstanceTab,
  reorderInstanceTabs,
  createAutogeneratedNote,
} from "../redux/slice";
import {
  selectInstanceTabs,
  selectInstanceActiveTab,
} from "../redux/selectors";
import { NoteTabItem } from "./NoteTabItem";

interface NoteTabBarProps {
  instanceId: string;
}

export function NoteTabBar({ instanceId }: NoteTabBarProps) {
  const dispatch = useAppDispatch();

  const openTabs = useAppSelector(selectInstanceTabs(instanceId));
  const activeTabId = useAppSelector(selectInstanceActiveTab(instanceId));
  const userId = useAppSelector((s) => s.user?.id);

  const scrollRef = useRef<HTMLDivElement>(null);
  const hydratedRef = useRef(false);
  const lastPushedUrlRef = useRef("");

  // ── URL → Redux: hydrate ONCE on mount (read URL directly, no hook) ──
  useEffect(() => {
    if (hydratedRef.current) return;
    hydratedRef.current = true;

    // Read URL params directly — no useSearchParams hook (avoids re-renders)
    const params = new URLSearchParams(window.location.search);
    const tabsParam = params.get("tabs");
    if (!tabsParam) return;

    const tabIds = tabsParam.split(",").filter(Boolean);
    if (tabIds.length === 0) return;

    tabIds.forEach((id) => {
      dispatch(addInstanceTab({ instanceId, noteId: id }));
    });

    const activeParam = params.get("active");
    if (activeParam && tabIds.includes(activeParam)) {
      dispatch(setInstanceActiveTab({ instanceId, noteId: activeParam }));
    } else {
      dispatch(setInstanceActiveTab({ instanceId, noteId: tabIds[0] }));
    }
  }, [dispatch, instanceId]);

  // ── Redux → URL: push tab state to URL (NO searchParams in deps) ────
  useEffect(() => {
    if (!hydratedRef.current) return;

    const params = new URLSearchParams(window.location.search);

    if (openTabs && openTabs.length > 0) {
      params.set("tabs", openTabs.join(","));
    } else {
      params.delete("tabs");
    }

    if (activeTabId) {
      params.set("active", activeTabId);
    } else {
      params.delete("active");
    }

    const newUrl = `${window.location.pathname}?${params.toString()}`;

    // Only push if URL actually changed (prevents infinite loop)
    if (newUrl !== lastPushedUrlRef.current) {
      lastPushedUrlRef.current = newUrl;
      window.history.replaceState(null, "", newUrl);
    }
  }, [openTabs, activeTabId]); // NO pathname, NO searchParams — only Redux state

  const handleNewTab = useCallback(() => {
    if (!userId) return;
    const id = crypto.randomUUID();
    dispatch(
      createAutogeneratedNote({
        id,
        userId,
        folder: "Draft",
        instanceId,
      }),
    );
  }, [dispatch, userId, instanceId]);

  // ── DnD reorder ─────────────────────────────────────────────────────

  const handleDragOver = useCallback((e: React.DragEvent) => {
    e.preventDefault();
    e.dataTransfer.dropEffect = "move";
  }, []);

  const handleDrop = useCallback(
    (e: React.DragEvent) => {
      e.preventDefault();
      const draggedId = e.dataTransfer.getData("text/plain");
      if (!draggedId || !openTabs) return;

      const container = scrollRef.current;
      if (!container) return;

      const tabElements = Array.from(
        container.querySelectorAll<HTMLElement>("[role='tab']"),
      );

      let insertIndex = openTabs.length;
      for (let i = 0; i < tabElements.length; i++) {
        const rect = tabElements[i].getBoundingClientRect();
        if (e.clientX < rect.left + rect.width / 2) {
          insertIndex = i;
          break;
        }
      }

      const currentIndex = openTabs.indexOf(draggedId);
      if (currentIndex === -1) return;

      const newOrder = openTabs.filter((id) => id !== draggedId);
      const adjusted =
        currentIndex < insertIndex ? insertIndex - 1 : insertIndex;
      newOrder.splice(adjusted, 0, draggedId);
      dispatch(reorderInstanceTabs({ instanceId, tabs: newOrder }));
    },
    [dispatch, instanceId, openTabs],
  );

  // ── Auto-scroll active tab into view ────────────────────────────────

  useEffect(() => {
    if (!scrollRef.current || !activeTabId) return;
    const el = scrollRef.current.querySelector(`[aria-selected="true"]`);
    el?.scrollIntoView({ behavior: "smooth", block: "nearest", inline: "center" });
  }, [activeTabId]);

  return (
    <div className="border-b border-border bg-muted/30 flex items-center shrink-0">
      <div
        ref={scrollRef}
        className="flex-1 flex items-end overflow-x-auto scrollbar-none gap-0.5 px-1 pt-1"
        onDragOver={handleDragOver}
        onDrop={handleDrop}
      >
        {openTabs?.map((tabId) => (
          <NoteTabItem key={tabId} noteId={tabId} instanceId={instanceId} />
        ))}
      </div>

      <button
        onClick={handleNewTab}
        className="flex items-center justify-center h-7 w-7 mx-1 rounded hover:bg-accent transition-colors flex-shrink-0 cursor-pointer"
        title="New note"
      >
        <Plus className="h-3.5 w-3.5 text-muted-foreground" />
      </button>
    </div>
  );
}
